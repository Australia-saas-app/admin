export function formatTravelPrice(listing: VisaTravelListing): string {
  return `$${listing.price.toLocaleString()} ${listing.currency}`
}

export function formatTravelResultCount(total: number): string {
  if (total >= 1000) return `${Math.round(total / 100) / 10}K`
  return String(total)
}
